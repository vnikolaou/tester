package com.pionware.lib;

public class Utils {
	public static long sum(long x, long y) {
		return x + y;
	}
	
	public static long multiply(long x, long y) {
		return x * y;
	}	
	
	public static long divide(long x, long y) {
		return x / y;
	}		
	
	public static long subtract(long x, long y) {
		return x - y;
	}	
}
